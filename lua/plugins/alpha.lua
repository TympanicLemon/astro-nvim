local function week_header(concat, append)
  local week = week_ascii_text()
  local daysoftheweek = { "Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday" }
  local day = daysoftheweek[os.date("*t").wday]
  local tbl = week[day]
  local qoute = " 匠心, 精益, 艺术, 科技; 创造, 创新, 不断改进"
  table.insert(tbl, os.date "%Y-%m-%d %H:%M:%S" .. (concat or " ") .. qoute)
  if append then vim.list_extend(tbl, append) end
  table.insert(tbl, "")
  return tbl
end

return {
  {
    "goolord/alpha-nvim",
    opts = function(_, opts)
      opts.section.header.val = week_header()
      opts.section.buttons.val = {
        opts.button("f", "Find Files", "<cmd>Telescope find_files<cr>"),
        opts.button("r", "Recent Files", "<cmd>Telescope oldfiles<cr>"),
        opts.button("g", "Live Grep", "<cmd>Telescope live_grep<cr>"),
        opts.button("l", "Lazy", "<cmd>Lazy<cr>"),
        opts.button("q", "Quit", "<cmd>qa!<cr>"),
      }
    end,
  },
}
